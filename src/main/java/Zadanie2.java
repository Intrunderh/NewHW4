public class Zadanie2 {
    // таблица умножения от 1 до 10 вложенным циклом
    public static void main(String[] args) {
        for(int i = 1; i <= 1; i++) {
            for(int j = 1; j <= 10; j++) {
                System.out.println(i + " * " + j + " = " + (i * j));
            }

        }
    }
}
